    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            int id = Integer.parseInt(txtId.getText());
            String name = txtName.getText();
            double price = Double.parseDouble(txtPrice.getText());
            double distanceTraveled = Double.parseDouble(txtDistance.getText());
            double totalPrice = price + (distanceTraveled * 0.1);
            double approximatedMileage = distanceTraveled * 1.1;

            MongoDBManager.uploadVehicleData(id, name, totalPrice, approximatedMileage);

            JOptionPane.showMessageDialog(this, "Vehicle data saved successfully!");

            btnClearActionPerformed(evt);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for all fields.", 
                                        "Invalid Input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

