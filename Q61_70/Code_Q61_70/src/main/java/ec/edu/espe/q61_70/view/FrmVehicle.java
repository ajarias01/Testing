    private void btnTotalPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalPriceActionPerformed
        try {
            double price = Double.parseDouble(txtPrice.getText());
            double distanceTraveled = Double.parseDouble(txtDistance.getText());

            double totalPrice = price + (distanceTraveled * 0.1);

            txtTotalPrice.setText(String.format("%.2f", totalPrice));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values for price and distance.");
        }
    }//GEN-LAST:event_btnTotalPriceActionPerformed

